/******************************************************************************
 class PnlIdecMisRec::StatShr
 ******************************************************************************/

PnlIdecMisRec::StatShr::StatShr(
			const uint ixIdecVExpstate
			, const string& scrJrefDetail
			, const string& scrJref1NTour
			, const string& scrJrefRef1NFile
			, const bool ButRegularizeActive
		) : Block() {
	this->ixIdecVExpstate = ixIdecVExpstate;
	this->scrJrefDetail = scrJrefDetail;
	this->scrJref1NTour = scrJref1NTour;
	this->scrJrefRef1NFile = scrJrefRef1NFile;
	this->ButRegularizeActive = ButRegularizeActive;

	mask = {IXIDECVEXPSTATE, SCRJREFDETAIL, SCRJREF1NTOUR, SCRJREFREF1NFILE, BUTREGULARIZEACTIVE};
};

bool PnlIdecMisRec::StatShr::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	string srefIxIdecVExpstate;

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatShrIdecMisRec");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemShrIdecMisRec";

	if (basefound) {
		if (extractStringAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "srefIxIdecVExpstate", srefIxIdecVExpstate)) {
			ixIdecVExpstate = VecIdecVExpstate::getIx(srefIxIdecVExpstate);
			add(IXIDECVEXPSTATE);
		};
		if (extractStringAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "scrJrefDetail", scrJrefDetail)) add(SCRJREFDETAIL);
		if (extractStringAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "scrJref1NTour", scrJref1NTour)) add(SCRJREF1NTOUR);
		if (extractStringAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "scrJrefRef1NFile", scrJrefRef1NFile)) add(SCRJREFREF1NFILE);
		if (extractBoolAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ButRegularizeActive", ButRegularizeActive)) add(BUTREGULARIZEACTIVE);
	};

	return basefound;
};

set<uint> PnlIdecMisRec::StatShr::comm(
			const StatShr* comp
		) {
	set<uint> items;

	if (ixIdecVExpstate == comp->ixIdecVExpstate) insert(items, IXIDECVEXPSTATE);
	if (scrJrefDetail.compare(comp->scrJrefDetail) == 0) insert(items, SCRJREFDETAIL);
	if (scrJref1NTour.compare(comp->scrJref1NTour) == 0) insert(items, SCRJREF1NTOUR);
	if (scrJrefRef1NFile.compare(comp->scrJrefRef1NFile) == 0) insert(items, SCRJREFREF1NFILE);
	if (ButRegularizeActive == comp->ButRegularizeActive) insert(items, BUTREGULARIZEACTIVE);

	return(items);
};

set<uint> PnlIdecMisRec::StatShr::diff(
			const StatShr* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {IXIDECVEXPSTATE, SCRJREFDETAIL, SCRJREF1NTOUR, SCRJREFREF1NFILE, BUTREGULARIZEACTIVE};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};

