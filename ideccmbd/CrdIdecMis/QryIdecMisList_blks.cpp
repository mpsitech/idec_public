/******************************************************************************
 class QryIdecMisList::VecVOrd
 ******************************************************************************/

uint QryIdecMisList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "tit") return TIT;
	else if (s == "grp") return GRP;
	else if (s == "own") return OWN;
	else if (s == "sta") return STA;

	return(0);
};

string QryIdecMisList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TIT) return("tit");
	else if (ix == GRP) return("grp");
	else if (ix == OWN) return("own");
	else if (ix == STA) return("sta");

	return("");
};

void QryIdecMisList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=4;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

