		writeStringAttr(wr, itemtag, "sref", "TxtShowing1", VecIdecVTag::getTitle(VecIdecVTag::SHOWSHORT, ixIdecVLocale));
		writeStringAttr(wr, itemtag, "sref", "TxtShowing2", VecIdecVTag::getTitle(VecIdecVTag::EMPSHORT, ixIdecVLocale));
