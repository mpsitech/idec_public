/******************************************************************************
 class QryIdecTouList::VecVOrd
 ******************************************************************************/

uint QryIdecTouList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "tit") return TIT;
	else if (s == "mis") return MIS;
	else if (s == "sta") return STA;

	return(0);
};

string QryIdecTouList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TIT) return("tit");
	else if (ix == MIS) return("mis");
	else if (ix == STA) return("sta");

	return("");
};

void QryIdecTouList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=3;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

